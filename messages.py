    # ─── 22. Форматированная дата ────────────────────────────────────────────

    @staticmethod
    def format_date(dt) -> str:
        """Форматирует дату в советском стиле."""
        if hasattr(dt, 'strftime'):
            return dt.strftime("%d.%m.%Y")
        return str(dt)

    @staticmethod
    def format_days_left(days: int) -> str:
        """Форматирует оставшиеся дни."""
        if days < 0:
            return f"истекла {abs(days)} дней назад"
        elif days == 0:
            return "истекает сегодня"
        elif days == 1:
            return "остался 1 день"
        else:
            return f"осталось {days} дней"